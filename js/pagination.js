function renderListPage(totalPages) {
    let html = "";
    html += `<li><a><i class="fas fa-angle-double-left btn-prev"></i></a></li>`;
    html += `<li class="active"><a>${1}</a></li>`;
    for (let i = 2; i <= totalPages; i++) {
      html += `<li><a>${i}</a></li>`;
    }
    if (totalPages === 0) {
      html = "";
    }
    html+='<li><a><i class="fas fa-angle-double-right btn-next"></i></a></li>';
    document.getElementById("number-page").innerHTML = html;
  };

