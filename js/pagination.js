function renderListPage(totalPages) {
  let html = "";
  html += `<li><a><i class="fas fa-angle-double-left btn-prev"></i></a></li>`;
  html += `<li class="active"><a>${1}</a></li>`;
  for (let i = 2; i <= totalPages; i++) {
    html += `<li><a>${i}</a></li>`;
  }
  if (totalPages === 0) {
    html = "";
  }
  html += '<li><a><i class="fas fa-angle-double-right btn-next"></i></a></li>';
  document.getElementById("number-page").innerHTML = html;
}

function getCurrentPage(indexPage) {
  start = (indexPage - 1) * perPage;
  end = indexPage * perPage;
  totalPages = Math.ceil(product.length / perPage);
}
$("#number-page li").on("click", () => {
  $("#number-page li").removeClass("active");
  const numPage = document.querySelector("#number-page li");
  numPage.classList.remove("active");
  getCurrentPage(idPage);
  $(`#number-page li:eq(${idPage - 1})`).addClass("active");
  getCurrentPage(idPage);
  renderProduct(product);
});
